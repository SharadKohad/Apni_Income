package model;

public class DirectNetworkModel
{
    String reg_date;
    String memb_name;
    String username;
    String m_country;
    String email;
    String level_no;
    String confirm_ph;
    String Place;
    String Total_Business;

    public String getReg_date() {
        return reg_date;
    }

    public void setReg_date(String reg_date) {
        this.reg_date = reg_date;
    }

    public String getMemb_name() {
        return memb_name;
    }

    public void setMemb_name(String memb_name) {
        this.memb_name = memb_name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getM_country() {
        return m_country;
    }

    public void setM_country(String m_country) {
        this.m_country = m_country;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLevel_no() {
        return level_no;
    }

    public void setLevel_no(String level_no) {
        this.level_no = level_no;
    }

    public String getConfirm_ph() {
        return confirm_ph;
    }

    public void setConfirm_ph(String confirm_ph) {
        this.confirm_ph = confirm_ph;
    }

    public String getPlace() {
        return Place;
    }

    public void setPlace(String place) {
        Place = place;
    }

    public String getTotal_Business() {
        return Total_Business;
    }

    public void setTotal_Business(String total_Business) {
        Total_Business = total_Business;
    }
}
